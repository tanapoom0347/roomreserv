<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1. ทางรีสอร์ท ยินดีต้อนรับสัตว์เลี้ยงของท่าน โดยจัดพื้นเฉพาะให้ โปรดแจ้งล่วงหน้า</p>
