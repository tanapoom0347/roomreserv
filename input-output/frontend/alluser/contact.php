  <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2. กรณีเด็กอายุไม่เกิน 3 ปี สามารถพักกับผู้ปกครองในห้อง Standard ได้ โดยไม่มีค่าใช้จ่าย</p>
  <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3. กรณีเด็ก 1 คน ใช้เตียงหรือโซฟาเตียงเสริมจะคิดค่าบริการในอัตราเตียงเสริม</p>
