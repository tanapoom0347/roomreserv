  <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;5. ในกรณีที่ต้องการคืนห้องพักหลังเวลา 11.00 น. มีค่าบริการ 100 บาท/ชั่วโมง/ห้อง เศษเวลาตั้งแต่ 15 นาทีขึ้นไปคิดเป็น 1 ชั่วโมง และหากคืนห้องพักเกินเวลา 17.00 น. มีค่าบริการเต็มวัน 1วันของค่าห้องพักปกติ</p>
  <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;6. ลูกค้าจะต้องชำระเงินค่ามัดครึ่งหนึ่งของราคารวมทั้งหมด และจะต้องชำระค่ามัดจำในวันที่จอง หากเกินเวลาที่กำหนดทางรีสอร์ทขอสงวนสิทธิ์ในการยกเลิกการจอง</p>
